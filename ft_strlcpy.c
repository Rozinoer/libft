#include "libft.h"

size_t		ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	unsigned int len;
	unsigned int i;

	if (!dst || !src)
		return (0);
	len = ft_strlen(src);
	i = 0;
	if (dstsize != 0)
	{
		while (src[i] != '\0' && i < (dstsize - 1))
		{
			dst[i] = src[i];
			i++;
		}
	}
	else
		return (len);
	dst[i] = '\0';
	return (len);
}
